def _clean_text(value: Any, default: str = "") -> str:
    text = str(value or "").strip()
    return text or default


def _display_date() -> str:
    now = datetime.now().astimezone()
    return f"{now.strftime('%B')} {now.day}, {now.year}"


def _truncate_text(value: str | None, limit: int) -> str:
    text = _clean_text(value)
    if len(text) <= limit:
        return text
    return f"{text[: max(0, limit - 1)].rstrip()}\u2026"


def _format_number(value: float | int) -> str:
    rounded = round(float(value), 3)
    if rounded.is_integer():
        return str(int(rounded))
    return f"{rounded:.3f}".rstrip("0").rstrip(".")


def _format_quantitative_value(node: ClaimNode) -> str:
    if node.quantitative_value is None:
        return "Not reported"
    unit = _clean_text(node.quantitative_unit)
    return f"{_format_number(node.quantitative_value)} {unit}".strip()


def _safe_ratio(value_a: float | None, value_b: float | None) -> float | None:
    if value_a is None or value_b is None:
        return None
    try:
        low = min(abs(float(value_a)), abs(float(value_b)))
        high = max(abs(float(value_a)), abs(float(value_b)))
    except (TypeError, ValueError):
        return None
    if high == 0:
        return None
    if low == 0:
        return high
    return high / low


def _ratio_label(ratio: float | None) -> str:
    if ratio is None:
        return "n/a"
    if ratio >= 10:
        return f"{ratio:.0f}x"
    return f"{ratio:.1f}x"


def _risk_bucket(score: float | None) -> str:
    value = float(score or 0.0)
    if value >= 0.85:
        return "critical"
    if value >= 0.60:
        return "high"
    if value >= 0.30:
        return "medium"
    return "low"


def _risk_badge(score: float | None) -> str:
    bucket = _risk_bucket(score)
    return f"{_TIER_LABELS[bucket].upper()} {float(score or 0.0):.2f}"


def _friction_distribution(nodes: list[ClaimNode]) -> dict[str, int]:
    distribution = {tier: 0 for tier in _TIER_ORDER}
    for node in nodes:
        distribution[_risk_bucket(node.friction_score)] += 1
    return distribution


def _node_compound_name(node: ClaimNode) -> str:
    candidates = [
        (node.subject_type, node.subject_name),
        (node.object_type, node.object_name),
        (None, node.subject_name),
        (None, node.object_name),
    ]
    for value_type, value in candidates:
        text = _clean_text(value)
        if not text:
            continue
        if value_type is None or "compound" in _clean_text(value_type).lower():
            return text
    return _clean_text(node.subject_name, "Unknown compound")


def _source_host_label(url: str | None) -> str:
    parsed = urlparse(_clean_text(url))
    host = parsed.netloc.lower().split(":")[0]
    if host.startswith("www."):
        host = host[4:]
    if not host:
        return "Source unavailable"
    mapping = {
        "europepmc.org": "Europe PMC",
        "semanticscholar.org": "Semantic Scholar",
        "youtube.com": "YouTube",
        "youtu.be": "YouTube",
        "doi.org": "DOI",
    }
    return mapping.get(host, host)


def _source_label(node: ClaimNode) -> str:
    if node.source_type == "private_csv":
        return _clean_text(node.file_name, "Private CSV")
    if node.source_type == "pdf_document":
        return _clean_text(node.file_name, "PDF document")
    if node.source_type == "web_url":
        return _source_host_label(node.abstract_url)
    if node.source_type == "youtube_video":
        return "YouTube"
    return _source_host_label(node.abstract_url)


def _journal_label(node: ClaimNode) -> str:
    return _clean_text(getattr(node, "journal", None)) or _source_label(node)


def _contradiction_source(node: ClaimNode) -> str:
    year = node.paper_year if node.paper_year is not None else "n.d."
    return f"{year}, {_journal_label(node)}"


def _pair_key(node: ClaimNode) -> tuple[str, str]:
    names = [
        _clean_text(node.subject_name, "Unknown entity"),
        _clean_text(node.object_name, "Unknown entity"),
    ]
    ordered = sorted(names, key=lambda value: value.lower())
    return ordered[0], ordered[1]


def _pair_stats(nodes: list[ClaimNode]) -> list[dict[str, Any]]:
    pair_map: dict[tuple[str, str], dict[str, Any]] = {}
    for node in nodes:
        if not node.contradicting_node_ids:
            continue
        entity_a, entity_b = _pair_key(node)
        record = pair_map.setdefault(
            (entity_a.lower(), entity_b.lower()),
            {
                "entity_a": entity_a,
                "entity_b": entity_b,
                "contradiction_count": 0,
                "max_friction": 0.0,
            },
        )
        record["contradiction_count"] += max(1, len(node.contradicting_node_ids))
        record["max_friction"] = max(record["max_friction"], float(node.friction_score or 0.0))
    return sorted(
        pair_map.values(),
        key=lambda item: (item["contradiction_count"], item["max_friction"], item["entity_a"], item["entity_b"]),
        reverse=True,
    )


def _friction_interpretation(distribution: dict[str, int], total_nodes: int) -> str:
    if total_nodes <= 0:
        return "No claim nodes were available, so no friction profile could be generated."

    hot_count = distribution["critical"] + distribution["high"]
    hot_pct = (hot_count / total_nodes) * 100
    if distribution["critical"] > 0:
        return (
            f"{hot_count} of {total_nodes} claims ({hot_pct:.1f}%) fall into the high-or-critical band, "
            "which indicates that the session contains a concentrated set of contradictions that should be "
            "validated before downstream target decisions are made."
        )
    if hot_count > 0:
        return (
            f"{hot_count} of {total_nodes} claims ({hot_pct:.1f}%) are high risk while none crossed the critical "
            "threshold, suggesting the map is directionally coherent but still contains assay or context mismatches "
            "that can materially change target confidence."
        )
    return (
        "Most claims remained in the medium-or-low band, which suggests the current session is comparatively "
        "coherent and that the remaining disagreements are more likely to be contextual than fatal."
    )


def _select_killer_line(nodes: list[ClaimNode], node_lookup: dict[str, ClaimNode]) -> str:
    scored_nodes = sorted(nodes, key=lambda node: float(node.friction_score or 0.0), reverse=True)

    def best_private_public(nodes_to_scan: list[ClaimNode]) -> str | None:
        for node in nodes_to_scan:
            if not node.contradicting_node_ids:
                continue
            linked_nodes = [
                node_lookup[other_id]
                for other_id in node.contradicting_node_ids
                if other_id in node_lookup
            ]
            for other in linked_nodes:
                sources = {node.source_type, other.source_type}
                if "private_csv" not in sources or "public_abstract" not in sources:
                    continue
                private_node = node if node.source_type == "private_csv" else other
                public_node = other if private_node is node else node
                ratio = _safe_ratio(private_node.quantitative_value, public_node.quantitative_value)
                if ratio is None:
                    continue
                published_nodes = [item for item in linked_nodes if item.source_type != "private_csv"]
                paper_count = len({item.paper_id or item.node_id for item in published_nodes}) or 1
                compound = _node_compound_name(private_node)
                cell_line = _clean_text(private_node.cell_line or public_node.cell_line, "the matched assay")
                return (
                    f"{paper_count} published papers report {compound} achieves "
                    f"{_format_quantitative_value(public_node)} in {cell_line}. Private assay data shows "
                    f"{_format_quantitative_value(private_node)} - a {_ratio_label(ratio)} discrepancy flagged as "
                    "Target Validation Risk."
                )
        return None

    critical_nodes = [node for node in scored_nodes if float(node.friction_score or 0.0) >= 0.85]
    killer_line = best_private_public(critical_nodes)
    if killer_line:
        return killer_line

    killer_line = best_private_public(scored_nodes)
    if killer_line:
        return killer_line

    for node in critical_nodes:
        contradicters = [
            node_lookup[other_id]
            for other_id in node.contradicting_node_ids
            if other_id in node_lookup
        ]
        quantified = [
            other for other in contradicters
            if other.quantitative_value is not None and node.quantitative_value is not None
        ]
        if not quantified:
            continue
        other = quantified[0]
        ratio = _safe_ratio(node.quantitative_value, other.quantitative_value)
        if ratio is None:
            continue
        cell_line = _clean_text(node.cell_line or other.cell_line, "the matched assay")
        compound = _node_compound_name(node)
        return (
            f"Critical claims place {compound} at {_format_quantitative_value(node)} versus "
            f"{_format_quantitative_value(other)} in {cell_line} - a {_ratio_label(ratio)} spread that keeps the "
            "pair above the Target Validation Risk threshold."
        )

    if critical_nodes:
        top = critical_nodes[0]
        return (
            f"The sharpest critical conflict is {top.subject_name} -> {top.object_name}, where "
            f"{len(top.contradicting_node_ids)} competing claims remain unresolved and warrant direct protocol "
            "harmonization before target commitment."
        )

    return (
        "No critical private-versus-published potency gap was present in this session, so the remaining risk is "
        "driven by contextual contradictions that still require follow-up experimentation."
    )


def _executive_summary(
    session_id: str,
    nodes: list[ClaimNode],
    distribution: dict[str, int],
    top_pairs: list[dict[str, Any]],
    node_lookup: dict[str, ClaimNode],
) -> list[str]:
    total_nodes = len(nodes)
    critical_count = distribution["critical"]
    high_count = distribution["high"]
    sentence_one = (
        f"Dialectic analyzed {total_nodes} claim nodes for session {session_id}, flagging {critical_count} as "
        f"critical and {high_count} additional claims as high risk."
    )
    if top_pairs:
        top_pair = top_pairs[0]
        sentence_two = (
            f"The densest contradiction cluster centers on {top_pair['entity_a']} and {top_pair['entity_b']}, "
            f"accumulating {top_pair['contradiction_count']} contradicted claims with a maximum friction score of "
            f"{top_pair['max_friction']:.2f}."
        )
    else:
        sentence_two = (
            "Contradictions are sparse across entity pairs, so the session is currently dominated by isolated risks "
            "rather than one repeated failure mode."
        )
    sentence_three = _select_killer_line(nodes, node_lookup)
    return [sentence_one, sentence_two, sentence_three]


def _unique_tamarind_records(nodes: list[ClaimNode]) -> list[dict[str, Any]]:
    records: dict[str, dict[str, Any]] = {}
    for node in nodes:
        verdict = node.tamarind_verdict if isinstance(node.tamarind_verdict, dict) else None
        if not verdict:
            continue
        key = _clean_text(verdict.get("tamarind_job_id"))
        if not key:
            key = "::".join(
                [
                    _clean_text(verdict.get("compound_a")),
                    _clean_text(verdict.get("compound_b")),
                    _clean_text(verdict.get("verdict")),
                    _clean_text(verdict.get("binding_affinity_a")),
                    _clean_text(verdict.get("binding_affinity_b")),
                ]
            )
        existing = records.get(key)
        record = {
            "key": key,
            "node": node,
            "verdict": verdict,
            "friction_score": float(node.friction_score or 0.0),
            "compound_a": _clean_text(verdict.get("compound_a"), _node_compound_name(node)),
            "compound_b": _clean_text(verdict.get("compound_b"), _clean_text(node.object_name, "Comparison claim")),
            "binding_affinity_a": verdict.get("binding_affinity_a"),
            "binding_affinity_b": verdict.get("binding_affinity_b"),
            "mock": bool(verdict.get("mock")),
        }
        if existing is None or record["friction_score"] > existing["friction_score"]:
            records[key] = record
    return list(records.values())


def compute_session_readiness(session_status: str, nodes: list[ClaimNode], progress: int = 0) -> dict[str, Any]:
    tamarind_jobs_total = sum(1 for node in nodes if float(node.friction_score or 0.0) >= 0.85)
    tamarind_jobs_done = 0
    tamarind_jobs_failed = 0
    for node in nodes:
        if float(node.friction_score or 0.0) < 0.85:
            continue
        verdict = node.tamarind_verdict if isinstance(node.tamarind_verdict, dict) else None
        if verdict is None:
            continue
        verdict_name = _clean_text(verdict.get("verdict")).lower()
        if verdict_name and verdict_name != "skipped":
            tamarind_jobs_done += 1
        else:
            tamarind_jobs_failed += 1

    debate_complete = session_status == "complete"
    nodes_with_quantitative = sum(1 for node in nodes if node.quantitative_value is not None)
    tamarind_ratio = (
        (tamarind_jobs_done + tamarind_jobs_failed) / tamarind_jobs_total
        if tamarind_jobs_total
        else 0.0
    )
    debate_ratio = 1.0 if debate_complete else max(0.0, min(float(progress or 0) / 100.0, 1.0))
    ready = (
        debate_complete
        and tamarind_jobs_total > 0
        and (tamarind_jobs_done + tamarind_jobs_failed) == tamarind_jobs_total
    )

    if ready:
        percent_complete = 100.0
    elif debate_complete:
        percent_complete = round(65.0 + (35.0 * tamarind_ratio), 1)
    else:
        percent_complete = round(65.0 * debate_ratio, 1)

    return {
        "ready": ready,
        "checks": {
            "debate_complete": debate_complete,
            "tamarind_jobs_total": tamarind_jobs_total,
            "tamarind_jobs_done": tamarind_jobs_done,
            "tamarind_jobs_failed": tamarind_jobs_failed,
            "nodes_total": len(nodes),
            "nodes_with_quantitative": nodes_with_quantitative,
        },
        "percent_complete": percent_complete,
    }


def _contradiction_sections(nodes: list[ClaimNode], node_lookup: dict[str, ClaimNode]) -> list[dict[str, Any]]:
    hot_nodes = [
        node for node in nodes
        if float(node.friction_score or 0.0) >= 0.60
    ]
    hot_nodes.sort(
        key=lambda node: (
            float(node.friction_score or 0.0),
            len(node.contradicting_node_ids),
            _clean_text(node.subject_name),
        ),
        reverse=True,
    )

    sections: list[dict[str, Any]] = []
    for node in hot_nodes:
        contradicters = [
            node_lookup[other_id]
            for other_id in node.contradicting_node_ids
            if other_id in node_lookup
        ]
        contradicters.sort(
            key=lambda other: (
                float(other.friction_score or 0.0),
                other.paper_year or 0,
                _clean_text(other.subject_name),
            ),
            reverse=True,
        )
        published_count = sum(1 for other in contradicters if other.source_type != "private_csv")
        contradiction_heading = (
            f"Contradicts {published_count} published claims"
            if published_count > 0
            else f"Contradicts {len(contradicters)} claims"
        )
        tamarind_verdict = node.tamarind_verdict if isinstance(node.tamarind_verdict, dict) else None
        sections.append(
            {
                "heading": f"{_clean_text(node.subject_name, 'Unknown subject')} \u2192 {_clean_text(node.object_name, 'Unknown object')}",
                "risk_badge": _risk_badge(node.friction_score),
                "risk_color": _TIER_COLORS[_risk_bucket(node.friction_score)],
                "claim_text": _clean_text(node.claim_text, "Claim text unavailable."),
                "polarity": _clean_text(node.polarity, "unknown"),
                "cell_line": _clean_text(node.cell_line, "Not reported"),
                "quantitative_value": _format_quantitative_value(node),
                "skeptic_rationale": _clean_text(node.skeptic_rationale),
                "contradiction_summary": contradiction_heading if contradicters else "",
                "contradictions": [
                    {
                        "claim_text": _clean_text(other.claim_text, "Claim text unavailable."),
                        "source": _contradiction_source(other),
                    }
                    for other in contradicters
                ],
                "tamarind_verdict": tamarind_verdict,
            }
        )
    return sections


