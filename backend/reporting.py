def _citation_entries(nodes: list[ClaimNode]) -> tuple[list[dict[str, Any]], bool]:
    citation_map: dict[str, dict[str, Any]] = {}
    mock_verdict_present = any(
        isinstance(node.tamarind_verdict, dict) and bool(node.tamarind_verdict.get("mock"))
        for node in nodes
    )
    for node in nodes:
        if not node.abstract_url:
            continue
        key = _clean_text(node.paper_id or node.abstract_url)
        if not key:
            continue
        current = citation_map.get(key)
        candidate = {
            "authors": _clean_text(node.paper_authors, "Unknown authors"),
            "year": node.paper_year,
            "claim_text": _truncate_text(_clean_text(node.claim_text), 80),
            "journal": _journal_label(node),
            "url": _clean_text(node.abstract_url),
            "citation_count": int(node.citation_count or 0),
        }
        if current is None or (
            (candidate["year"] or 0, candidate["citation_count"]) > (current["year"] or 0, current["citation_count"])
        ):
            citation_map[key] = candidate

    ordered = sorted(
        citation_map.values(),
        key=lambda item: (item["year"] is not None, item["year"] or 0, item["citation_count"]),
        reverse=True,
    )
    return ordered, mock_verdict_present


def _is_ic50_node(node: ClaimNode) -> bool:
    unit = _clean_text(node.quantitative_unit).lower()
    return node.quantitative_value is not None and ("nm" in unit or "ic50" in unit)


def _build_ic50_discrepancies(nodes: list[ClaimNode]) -> list[dict[str, Any]]:
    grouped: dict[str, dict[str, Any]] = {}
    for node in nodes:
        if not _is_ic50_node(node):
            continue
        compound = _node_compound_name(node)
        record = grouped.setdefault(
            compound.lower(),
            {
                "compound": compound,
                "private_values": [],
                "public_values": [],
            },
        )
        if node.source_type == "private_csv":
            record["private_values"].append(node)
        else:
            record["public_values"].append(node)

    rows: list[dict[str, Any]] = []
    for entry in grouped.values():
        if not entry["private_values"]:
            continue
        private_node = max(entry["private_values"], key=lambda node: float(node.quantitative_value or 0.0))
        public_node = (
            min(entry["public_values"], key=lambda node: float(node.quantitative_value or 0.0))
            if entry["public_values"]
            else None
        )
        fold = _safe_ratio(
            private_node.quantitative_value,
            public_node.quantitative_value if public_node else None,
        )
        high_risk = bool(fold is not None and fold > 10)
        rows.append(
            {
                "compound": entry["compound"],
                "private_ic50": _format_quantitative_value(private_node),
                "published_ic50": _format_quantitative_value(public_node) if public_node else "No matched literature",
                "fold_difference": _ratio_label(fold),
                "risk": "CRITICAL" if high_risk else "Monitor",
                "risk_color": "#ff3050" if high_risk else "#1f2430",
                "risk_bold": high_risk,
            }
        )
    rows.sort(key=lambda item: (item["risk"] == "CRITICAL", item["fold_difference"], item["compound"]), reverse=True)
    return rows


def _build_structural_sections(nodes: list[ClaimNode]) -> list[dict[str, Any]]:
    records = [
        record for record in _unique_tamarind_records(nodes)
        if record["friction_score"] >= 0.85
    ]
    records.sort(key=lambda item: item["friction_score"], reverse=True)

    sections: list[dict[str, Any]] = []
    for record in records:
        verdict = record["verdict"]
        verdict_name = _clean_text(verdict.get("verdict"), "inconclusive")
        affinity_a = verdict.get("binding_affinity_a")
        affinity_b = verdict.get("binding_affinity_b")
        stronger = "Equal affinity"
        if affinity_a is not None and affinity_b is not None:
            if affinity_a > affinity_b:
                stronger = "Node A stronger"
            elif affinity_b > affinity_a:
                stronger = "Node B stronger"

        sections.append(
            {
                "heading": f"{record['compound_a']} vs {record['compound_b']}",
                "verdict": verdict_name,
                "verdict_display": verdict_name.replace("_", " ").upper(),
                "verdict_color": _TAMARIND_VERDICT_COLORS.get(verdict_name, "#ff8c00"),
                "structural_rationale": _clean_text(verdict.get("structural_rationale"), "No structural rationale reported."),
                "binding_rows": [
                    {
                        "label": "Node A claim",
                        "affinity": _format_number(affinity_a) if affinity_a is not None else "Not reported",
                        "interpretation": (
                            "stronger" if affinity_a is not None and affinity_b is not None and affinity_a > affinity_b
                            else "weaker" if affinity_a is not None and affinity_b is not None and affinity_a < affinity_b
                            else stronger.lower()
                        ),
                    },
                    {
                        "label": "Node B claim",
                        "affinity": _format_number(affinity_b) if affinity_b is not None else "Not reported",
                        "interpretation": (
                            "stronger" if affinity_a is not None and affinity_b is not None and affinity_b > affinity_a
                            else "weaker" if affinity_a is not None and affinity_b is not None and affinity_b < affinity_a
                            else stronger.lower()
                        ),
                    },
                ],
                "confidence": f"{round(float(verdict.get('confidence') or 0.0) * 100):.0f}%",
                "job_note": (
                    "Docking performed via Tamarind Bio DiffDock API. "
                    f"Protein: KRAS G12C (PDB: 6OIM). Job ID: {_clean_text(verdict.get('tamarind_job_id'), 'n/a')}"
                ),
                "mock": bool(verdict.get("mock")),
            }
        )
    return sections


