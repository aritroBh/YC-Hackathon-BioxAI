def _run_validation(document_bytes: bytes) -> bool:
    if not _VALIDATE_SCRIPT.exists():
        return False

    with tempfile.TemporaryDirectory(prefix="dialectic-docx-") as temp_dir:
        report_path = Path(temp_dir) / "report.docx"
        report_path.write_bytes(document_bytes)
        process = subprocess.run(
            ["python3", str(_VALIDATE_SCRIPT), str(report_path)],
            cwd=str(_ROOT_DIR),
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
            check=False,
        )
        return process.returncode == 0


def render_report_docx(payload: dict[str, Any]) -> tuple[bytes, bool]:
    if not _REPORT_SCRIPT.exists():
        raise FileNotFoundError(f"Report generator script was not found at {_REPORT_SCRIPT}.")

    process = subprocess.run(
        ["node", str(_REPORT_SCRIPT)],
        cwd=str(_FRONTEND_DIR),
        input=json.dumps(payload, ensure_ascii=False).encode("utf-8"),
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        check=False,
    )
    if process.returncode != 0:
        error_text = process.stderr.decode("utf-8", errors="replace").strip() or "Unknown docx generation error."
        raise RuntimeError(error_text)
    if not process.stdout:
        raise RuntimeError("Report generation completed without producing a document.")

    validation_ok = _run_validation(process.stdout)
    return process.stdout, validation_ok
