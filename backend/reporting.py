def _generate_contradiction_network(path: Path, nodes: list[ClaimNode]) -> None:
    plt, nx, _ = _import_chart_modules()
    graph = nx.Graph()
    node_citations: dict[str, int] = defaultdict(int)
    node_risk: dict[str, str] = defaultdict(lambda: "low")
    edge_weights: dict[tuple[str, str], float] = {}

    for node in nodes:
        if not node.contradicting_node_ids:
            continue
        subject = _clean_text(node.subject_name, "Unknown subject")
        obj = _clean_text(node.object_name, "Unknown object")
        for name in (subject, obj):
            node_citations[name] = max(node_citations[name], int(node.citation_count or 0))
            current_risk = node_risk[name]
            incoming_risk = _risk_bucket(node.friction_score)
            if _TIER_ORDER.index(incoming_risk) < _TIER_ORDER.index(current_risk):
                node_risk[name] = incoming_risk
        edge_key = tuple(sorted((subject, obj)))
        edge_weights[edge_key] = max(edge_weights.get(edge_key, 0.0), float(node.friction_score or 0.0))

    fig, ax = plt.subplots(figsize=(10, 8))
    if not edge_weights:
        _render_empty_chart(ax, "Contradiction Network - Entity Relationships", "No contradiction edges available.")
        _save_figure(fig, path)
        plt.close(fig)
        return

    _apply_dark_theme(ax, "Contradiction Network - Entity Relationships")
    for (entity_a, entity_b), weight in edge_weights.items():
        graph.add_edge(entity_a, entity_b, weight=weight * 3)

    positions = nx.spring_layout(graph, seed=42, k=2.0)
    raw_sizes = [node_citations.get(name, 0) for name in graph.nodes]
    max_size = max(raw_sizes) if raw_sizes else 1
    min_size = min(raw_sizes) if raw_sizes else 0

    def scaled_size(value: int) -> float:
        if max_size == min_size:
            return 300
        return 100 + (((value - min_size) / (max_size - min_size)) * 700)

    node_sizes = [scaled_size(node_citations.get(name, 0)) for name in graph.nodes]
    node_colors = []
    for name in graph.nodes:
        risk = node_risk.get(name, "low")
        if risk == "critical":
            node_colors.append("#ff3050")
        elif risk == "high":
            node_colors.append("#ff8c00")
        else:
            node_colors.append("#00e5a0")

    nx.draw_networkx_edges(
        graph,
        positions,
        ax=ax,
        width=[graph[edge_a][edge_b]["weight"] for edge_a, edge_b in graph.edges],
        edge_color="#6b7590",
        alpha=0.65,
    )
    nx.draw_networkx_nodes(
        graph,
        positions,
        ax=ax,
        node_size=node_sizes,
        node_color=node_colors,
        edgecolors="#0c0e12",
        linewidths=1.2,
    )
    nx.draw_networkx_labels(graph, positions, ax=ax, font_size=8, font_color="white")
    ax.set_xticks([])
    ax.set_yticks([])
    _save_figure(fig, path)
    plt.close(fig)


def _build_chart_manifest(chart_dir: Path, nodes: list[ClaimNode], warnings: list[str]) -> dict[str, dict[str, Any]]:
    chart_paths: dict[str, dict[str, Any]] = {}
    generators = {
        "friction_distribution_bar": _generate_friction_distribution_chart,
        "ic50_comparison_bar": _generate_ic50_comparison_chart,
        "tamarind_verdicts_pie": _generate_tamarind_pie_chart,
        "binding_affinity_scatter": _generate_binding_affinity_scatter,
        "contradiction_network": _generate_contradiction_network,
    }
    for chart_key, generator in generators.items():
        spec = _CHART_SPECS[chart_key]
        path = chart_dir / spec["filename"]
        try:
            generator(path, nodes)
            chart_paths[chart_key] = {
                "path": str(path),
                "width_inches": spec["width_inches"],
                "height_inches": spec["height_inches"],
            }
        except Exception as exc:
            LOGGER.warning("Chart generation failed for %s: %s", chart_key, exc)
            warnings.append(f"{chart_key}: {exc}")
    return chart_paths


