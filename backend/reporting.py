def _generate_ic50_comparison_chart(path: Path, nodes: list[ClaimNode]) -> None:
    plt, _, _ = _import_chart_modules()
    ic50_nodes = [node for node in nodes if _is_ic50_node(node)]
    grouped: dict[str, list[ClaimNode]] = defaultdict(list)
    for node in ic50_nodes:
        grouped[_node_compound_name(node)].append(node)

    fig, ax = plt.subplots(figsize=(10, 5))
    if not grouped:
        _render_empty_chart(ax, "IC50 Comparison: Private Assay vs Published Literature", "No IC50 measurements available.")
        _save_figure(fig, path)
        plt.close(fig)
        return

    _apply_dark_theme(ax, "IC50 Comparison: Private Assay vs Published Literature")
    compounds = sorted(grouped.keys())
    group_gap = 1.2
    x_positions: list[float] = []
    x_labels: list[str] = []
    x_meta: dict[str, list[tuple[float, ClaimNode]]] = {}
    cursor = 0.0
    for compound in compounds:
        members = sorted(
            grouped[compound],
            key=lambda node: (node.source_type != "private_csv", float(node.quantitative_value or 0.0)),
        )
        x_meta[compound] = []
        for index, node in enumerate(members):
            xpos = cursor + index
            x_positions.append(xpos)
            x_labels.append(compound if index == 0 else "")
            x_meta[compound].append((xpos, node))
        cursor += max(1, len(members)) + group_gap

    for compound in compounds:
        for xpos, node in x_meta[compound]:
            color = "#ff8c00" if node.source_type == "private_csv" else "#00c8ff"
            ax.bar(xpos, float(node.quantitative_value or 0.0), color=color, width=0.7)

        private_values = [node for _, node in x_meta[compound] if node.source_type == "private_csv"]
        public_values = [node for _, node in x_meta[compound] if node.source_type != "private_csv"]
        if private_values and public_values:
            private_peak = max(float(node.quantitative_value or 0.0) for node in private_values)
            public_floor = min(float(node.quantitative_value or 0.0) for node in public_values)
            if public_floor > 0 and private_peak > public_floor * 10:
                left = x_meta[compound][0][0] - 0.2
                right = x_meta[compound][-1][0] + 0.2
                y_top = private_peak * 1.25
                y_mid = public_floor * 1.05
                ax.plot([left, left, right, right], [y_mid, y_top, y_top, y_mid], color="#ff3050", linewidth=1.2)
                ax.text(
                    (left + right) / 2,
                    y_top * 1.06,
                    f"{round(private_peak / public_floor):.0f}x discrepancy",
                    color="#ff3050",
                    fontsize=8,
                    ha="center",
                )

    ax.set_yscale("log")
    ax.axhline(10, color="#ff3050", linestyle="--", linewidth=1)
    ax.text(0.01, 10, "1 nM threshold", color="#ff3050", fontsize=8, va="bottom", transform=ax.get_yaxis_transform())
    ax.set_ylabel("IC50 (nM, log scale)", color="white")
    ax.set_xticks(x_positions)
    ax.set_xticklabels(x_labels, rotation=35, ha="right", color="white", fontsize=8)
    ax.legend(
        handles=[
            plt.Rectangle((0, 0), 1, 1, color="#ff8c00", label="private_csv"),
            plt.Rectangle((0, 0), 1, 1, color="#00c8ff", label="public_abstract"),
        ],
        facecolor="#0c0e12",
        edgecolor="#1e2430",
        labelcolor="white",
        fontsize=8,
    )
    _save_figure(fig, path)
    plt.close(fig)


def _generate_tamarind_pie_chart(path: Path, nodes: list[ClaimNode]) -> None:
    plt, _, _ = _import_chart_modules()
    verdict_order = ["node_a_supported", "node_b_supported", "both_plausible", "inconclusive", "skipped"]
    counts = Counter(
        _clean_text(record["verdict"].get("verdict"), "skipped")
        for record in _unique_tamarind_records(nodes)
    )
    fig, ax = plt.subplots(figsize=(6, 6))
    if not counts:
        _render_empty_chart(ax, "Structural Arbitration Outcomes (Tamarind DiffDock)", "No Tamarind verdicts available.")
        _save_figure(fig, path)
        plt.close(fig)
        return

    ax.set_facecolor("#0c0e12")
    fig.patch.set_facecolor("#0c0e12")
    labels = []
    sizes = []
    colors = []
    total = sum(counts.values()) or 1
    for verdict in verdict_order:
        count = counts.get(verdict, 0)
        if count <= 0:
            continue
        labels.append(f"{verdict}\n{count} ({(count / total) * 100:.0f}%)")
        sizes.append(count)
        colors.append(_TAMARIND_VERDICT_COLORS[verdict])

    ax.pie(
        sizes,
        labels=labels,
        colors=colors,
        textprops={"color": "white", "fontsize": 8},
        startangle=90,
        wedgeprops={"linewidth": 1, "edgecolor": "#0c0e12"},
    )
    ax.set_title("Structural Arbitration Outcomes (Tamarind DiffDock)", color="white", fontsize=13, pad=14)
    _save_figure(fig, path)
    plt.close(fig)


